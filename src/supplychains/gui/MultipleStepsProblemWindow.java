    private void calculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateActionPerformed
        try {
            ArrayList<Supplier> suppliers = getSuppliersList();
            ArrayList<Customer> customers = getCustomersList();
            TransportConnection[][] transportConnections = getTransportConnectionsMatrix(suppliers, customers);
            SupplyChains solver = new SupplyChains();
            solver.setLogger(getLogger());
            solver.setConnections(transportConnections);
            solver.setCustomers(customers);
            solver.setSuppliers(suppliers);
            solver.solve2();
            showResults(solver.getConnectionCosts());
        } catch (SupplyChainsException ex) {
            Logger.getLogger(BrokerProblemWindow.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_calculateActionPerformed


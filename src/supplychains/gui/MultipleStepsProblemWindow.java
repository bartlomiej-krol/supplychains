    private boolean isSupplier(int row, int max) {
        for(int i = 0; i < max; i++)
            if((int)spinConnections[row][i].getValue() > 0)
                return true;
        return false;
    }
    
    private boolean isCustomer(int col, int max) {
        for(int i = 0; i < max; i++)
            if((int)spinConnections[i][col].getValue() > 0)
                return true;
        return false;
    }
    
    private int calculateBuffor(int n) {
        int sum = 0;
        for (int i = 0; i < n; i++) {
            int val = (int)spinNodesSupply[i].getValue();
            if(val > 0) sum += val;
        }
        return sum;
    }
    
    private ArrayList<Supplier> getSuppliersList() {
        ArrayList<Supplier> list = new ArrayList();
        int n = (int)nodesNumber.getValue();
        int buffor = calculateBuffor(n);
        for (int i = 0; i < n; i++) {
            if(isSupplier(i, n)) {
                Supplier s;
                int val = (int)spinNodesSupply[i].getValue();
                if(!isCustomer(i, n))
                    s = new Supplier(Math.abs(val), 0);
                else
                    s = new Supplier(Math.abs(Math.max(0, val)) + buffor, 0);
                list.add(s);
            }
        }
        return list;
    }

    private ArrayList<Customer> getCustomersList() { 
        ArrayList<Customer> list = new ArrayList();
        int n = (int)nodesNumber.getValue();
        int buffor = calculateBuffor(n);
        for (int i = 0; i < n; i++) {
            if(isCustomer(i, n)) {
                Customer c;
                int val = (int)spinNodesSupply[i].getValue();
                if(!isSupplier(i, n))
                    c = new Customer(Math.abs(val), 0);
                else
                    c = new Customer(Math.abs(Math.min(0,val)) + buffor, 0);
                list.add(c);
            }
        }
        return list;
    }

    private TransportConnection[][] getTransportConnectionsMatrix(ArrayList<Supplier> suppliers, ArrayList<Customer> customers) {
        int n = (int)nodesNumber.getValue();
        TransportConnection[][] cons = new TransportConnection[suppliers.size()][customers.size()];
        
        int k = 0, l;
        for (int i = 0; i < n; i++) {
            if(!isSupplier(i, n)) continue;
            l = 0;
            for (int j = 0; j < n; j++) {
                if(!isCustomer(j, n)) continue;
                int val = (int)spinConnections[i][j].getValue();
                if(val > 0 || i == j) {
                    cons[k][l] = new TransportConnection(suppliers.get(k), customers.get(l), val, k, l);
                } else {
                    cons[k][l] = new TransportConnection(suppliers.get(k), customers.get(l), 0, k, l);
                    cons[k][l].blocked = true;
                }
                l++;
            }
            k++;
        }
        return cons;
    }

    private void showResults(TransportConnection[][] matrix) {
        String results = "\n\n=======================================================\n";  
        results = "\n======\nWyniki\n======\n";        
        int N = matrix.length;
        int M = matrix[0].length;
        
        results += "\nIlości:\n";
        for(int i = 0; i < N; i++) {
            for(int j = 0; j < M; j++) {
                results += String.format("%5d", (int)matrix[i][j].amount);
            }
            results += "\n";
        }
        
        results += "\n";
        results += "Koszty = " + SupplyChainsUtils.countTotalCosts(matrix);
        results += "\n";
        
        getLogger().info(results);
    }

    private void refreshGUI() {
        int n = (int) nodesNumber.getValue();
        for(int i = 0; i < N; i++) {               
            if(i < n) spinNodesSupply[i].setEnabled(true);
            else spinNodesSupply[i].setEnabled(false);
            
            for(int j = 0; j < N; j++) {
                if(i < n && j < n) spinConnections[i][j].setEnabled(true);
                else spinConnections[i][j].setEnabled(false);
            }
        }
    }

    private SupplyChainsLogger getLogger() {
        if(logger != null) return logger;
        logger = new SupplyChainsLogger() {
            @Override
            public void info(String s) {
                logArea.setText(logArea.getText() + s + "\n");
                System.out.println(s);
            }
            
            @Override
            public void error(String s, Exception e) {
                logArea.setText(logArea.getText() + s + "\n");
                logArea.setText(logArea.getText() + e.getMessage() + "\n");
                System.out.println(s);
                System.out.println(e.getMessage());
            }
        };
        return logger;
    }

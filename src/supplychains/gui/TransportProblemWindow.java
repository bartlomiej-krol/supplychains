    private void refreshGUI() {
        int n = (int) suppliersNumber.getValue();
        int m = (int) customersNumber.getValue();
        for(int i = 0; i < N; i++) {
            for(int j = 0; j < M; j++) {                
                if(i < n && j < m) spinConnections[i][j].setEnabled(true);
                else spinConnections[i][j].setEnabled(false);
            }
        }
        for(int i = 0; i < N; i++) {               
            if(i < n) spinSuppliersSupply[i].setEnabled(true);
            else spinSuppliersSupply[i].setEnabled(false);
            if(i < n) cbSuppliersPrivilages[i].setEnabled(true);
            else cbSuppliersPrivilages[i].setEnabled(false);
        }
        for(int i = 0; i < M; i++) {               
            if(i < m) spinCustomersDemand[i].setEnabled(true);
            else spinCustomersDemand[i].setEnabled(false);
            if(i < m) cbCustomersPrivilages[i].setEnabled(true);
            else cbCustomersPrivilages[i].setEnabled(false);
        }
    }

    private SupplyChainsLogger getLogger() {
        if(logger != null) return logger;
        logger = new SupplyChainsLogger() {
            @Override
            public void info(String s) {
                logArea.setText(logArea.getText() + s + "\n");
                System.out.println(s);
            }
            
            @Override
            public void error(String s, Exception e) {
                logArea.setText(logArea.getText() + s + "\n");
                logArea.setText(logArea.getText() + e.getMessage() + "\n");
                System.out.println(s);
                System.out.println(e.getMessage());
            }
        };
        return logger;
    }

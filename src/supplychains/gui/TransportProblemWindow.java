    private ArrayList<Supplier> getSuppliersList() {
        int n = (int) suppliersNumber.getValue();
        ArrayList<Supplier> list = new ArrayList();
        for(int i = 0; i < n; i++) {
            Supplier s = new Supplier((int)spinSuppliersSupply[i].getValue(), 0);
            s.isPrivileged = cbSuppliersPrivilages[i].isSelected();
            list.add(s);
        }
        return list;
    }

    private ArrayList<Customer> getCustomersList() {  
        int m = (int) customersNumber.getValue();
        ArrayList<Customer> list = new ArrayList();
        for(int i = 0; i < m; i++) {
            Customer c = new Customer((int)spinCustomersDemand[i].getValue(), 0);
            c.isPrivileged = cbCustomersPrivilages[i].isSelected();
            list.add(c);
        }
        return list;
    }

    private TransportConnection[][] getTransportConnectionsMatrix(ArrayList<Supplier> suppliers, ArrayList<Customer> customers) {
        int n = (int) suppliersNumber.getValue();
        int m = (int) customersNumber.getValue();
        TransportConnection[][] cons = new TransportConnection[n][m];
        for(int i = 0; i < n; i++) {
            for(int j = 0; j < m; j++) {
                cons[i][j] = new TransportConnection(suppliers.get(i), customers.get(j), (int)spinConnections[i][j].getValue(),i,j);
            }
        }
        return cons;
    }

